#include <iostream>
using namespace std;

int main() {
    int angk = 0;

    do
    {
        cout << "nilai ke: " << angk << endl;
        angk++;
    } while (angk > 10);
    //bedanya while do dan while
    /*
    while akan menjalankan program selagi kondisi benar
    do while akan menjalankan minimal sekali jika kondsi benar atau salah
    */
    return 0;
}